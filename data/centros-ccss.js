// Función para obtener centros por tipo
function getCentrosPorTipo(tipo) {
  return CENTROS_CCSS[tipo] || [];
}

// Función de búsqueda en centros
function buscarCentros(query, tipo = null) {
  const queryLower = query.toLowerCase();
  let centros = [];
  
  if (tipo) {
    centros = CENTROS_CCSS[tipo] || [];
  } else {
    // Buscar en todos los tipos
    centros = [
      ...CENTROS_CCSS.ebais,
      ...CENTROS_CCSS.clinicas,
      ...CENTROS_CCSS.areas,
      ...CENTROS_CCSS.hospitales
    ];
  }
  
  return centros.filter(centro => 
    centro.toLowerCase().includes(queryLower)
  );
}
